package softpass;

public class BematechPrinterException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -4247986264264116988L;

	public BematechPrinterException() {
		super();
	}

	public BematechPrinterException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public BematechPrinterException(String message, Throwable cause) {
		super(message, cause);
	}

	public BematechPrinterException(String message) {
		super(message);
	}

	public BematechPrinterException(Throwable cause) {
		super(cause);
	}

	
}
